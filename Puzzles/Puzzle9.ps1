Greetings Iron Scripters. The countdown to Iron Scripter is accelerating with very little time before the main event.

In this weeks challenge you’re required to:
    • Discover the size of the TEMP folder on the local system – report total size, number of subfolders and number of files
    • Clean out the contents of the TEMP folder that are more than 24 hours old
    • Ensure that the deletion process doesn’t error by EXCLUDING files and directories that are pinned open. You may not be able to discover these files except by trial and error
    • Empty the recycle bin for the drive containing your TEMP folder
    • Create a scheduled task or job that will automate this process. Store the total size, number of files, folders and timestamp in a log file before and after the clean-up action.
        Also log the success or failure of the scheduled task/job. If you use scheduled jobs ensure that the old jobs are removed on a scheduled basis

Use best practice if it doesn’t conflict with your faction’s aims.
The solution must be acceptable to your faction:
• Daybreak Faction - beautiful code
• Flawless Faction - flawless code
• Battle Faction - good enough to get the job done
Good luck and good coding.
